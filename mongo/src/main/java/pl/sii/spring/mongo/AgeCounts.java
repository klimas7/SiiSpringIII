package pl.sii.spring.mongo;

public class AgeCounts {
    private Integer age;
    private Integer count;

    public AgeCounts() {
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
}
